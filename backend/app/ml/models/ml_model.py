# 1. SETUP & DATA LOADING
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
from sklearn.model_selection import train_test_split, RandomizedSearchCV
from sklearn.preprocessing import StandardScaler
from sklearn.linear_model import LinearRegression, Ridge
from sklearn.svm import SVR
from sklearn.neighbors import KNeighborsRegressor
from sklearn.tree import DecisionTreeRegressor
from sklearn.ensemble import RandomForestRegressor
from sklearn.metrics import mean_absolute_error
import joblib

# Load datasets
files = ["Haro Maya_6_East Hararge_Oromiya.csv", "Deder_11_East Hararge_Oromiya.csv",
         "Bedeno_13_East Hararge_Oromiya.csv", "Babile_4_East Hararge_Oromiya.csv",
         "Chinaksen_15_East Hararge_Oromiya.csv"]

print("🔄 Loading datasets...")
try:
    df = pd.concat([pd.read_csv(f) for f in files], ignore_index=True)
except FileNotFoundError:
    print("❌ Error: CSV files not found. Ensure datasets are uploaded to the environment.")

df['Date'] = pd.to_datetime(df['Date'])
df['month'] = df['Date'].dt.month
df = df.sort_values(['district', 'Date'])

# 2. FEATURE ENGINEERING
print("🧠 Engineering features...")
dist_month_map = df.groupby(['district', 'month'])['PRECTOTCORR'].mean().to_dict()
df['hist_norm_rain'] = df.set_index(['district', 'month']).index.map(dist_month_map)
df['rain_30d_cum'] = df.groupby('district')['PRECTOTCORR'].transform(lambda x: x.rolling(30).sum())
df['press_trend'] = df.groupby('district')['PS'].diff()
df['hum_trend'] = df.groupby('district')['RH2M'].diff()
df['yesterday_rain'] = df.groupby('district')['PRECTOTCORR'].shift(1)
df = df.dropna()

# 3. DATA PREPARATION
y_raw = df['PRECTOTCORR']
y_log = np.log1p(y_raw)
basic_f = ['TS', 'RH2M', 'PS', 'WS2M', 'CLOUD_AMT']
adv_f = basic_f + ['GWETTOP', 'QV2M', 'hist_norm_rain', 'rain_30d_cum', 'press_trend', 'hum_trend', 'yesterday_rain', 'month']

X_train_adv, X_test_adv, y_train_log, y_test_log = train_test_split(df[adv_f], y_log, test_size=0.2, random_state=42)
_, _, _, y_test_raw = train_test_split(df[adv_f], y_raw, test_size=0.2, random_state=42)

scaler = StandardScaler()
X_train_adv_sc = scaler.fit_transform(X_train_adv)
X_test_adv_sc = scaler.transform(X_test_adv)

# 4. THE COMPREHENSIVE ALGORITHM TOURNAMENT
print("⚔️ Benchmarking Proposed Model against Traditional Methods...")

# Absolute Baseline (Linear Regression)
baseline = LinearRegression()
baseline.fit(X_train_adv[basic_f], np.expm1(y_train_log))
mae_base = mean_absolute_error(y_test_raw, baseline.predict(X_test_adv[basic_f]))

# Tournament configurations
model_configs = {
    "Ridge Regression (Traditional)": {
        "model": Ridge(),
        "params": {'alpha': [1.0, 10.0]}
    },
    "Decision Tree (Traditional)": {
        "model": DecisionTreeRegressor(random_state=42),
        "params": {'max_depth': [5, 10]}
    },
    "K-Nearest Neighbors (KNN)": {
        "model": KNeighborsRegressor(),
        "params": {'n_neighbors': [5, 11]}
    },
    "Support Vector Regressor (SVR)": {
        "model": SVR(kernel='rbf'),
        "params": {'C': [1, 10]}
    },
    "Random Forest (Proposed)": {
        "model": RandomForestRegressor(random_state=42, n_jobs=-1),
        "params": {'n_estimators': [50], 'max_depth': [15]}
    }
}

# Start results list with the baseline correctly typed
results = [{"Algorithm": "Linear Regression (Baseline)", "MAE": mae_base, "Type": "Traditional"}]
best_estimators = {}

for name, config in model_configs.items():
    print(f"  ...Benchmarking {name}")
    search = RandomizedSearchCV(config["model"], config["params"], n_iter=2, cv=2,
                                scoring='neg_mean_absolute_error', n_jobs=-1, random_state=42)
    search.fit(X_train_adv_sc, y_train_log)
    best_model = search.best_estimator_
    best_estimators[name] = best_model

    preds_raw = np.expm1(best_model.predict(X_test_adv_sc))
    mae = mean_absolute_error(y_test_raw, preds_raw)

    # Logic to classify the Type and avoid NaN in the final table
    m_type = "Advanced" if ("Proposed" in name or "SVR" in name) else "Traditional"
    results.append({"Algorithm": name, "MAE": mae, "Type": m_type})

# 5. FINAL PERFORMANCE HIERARCHY
res_df = pd.DataFrame(results).sort_values('MAE', ascending=False)
res_df['Efficiency Improvement (%)'] = ((mae_base - res_df['MAE']) / mae_base * 100).round(2)

print("\n" + "="*85)
print("FINAL ALGORITHM PERFORMANCE HIERARCHY")
print(res_df.to_string(index=False))
print("="*85)

# Visualizing the Performance Gap
plt.figure(figsize=(12, 6))
sns.barplot(x='MAE', y='Algorithm', data=res_df, palette='viridis', hue='Algorithm', legend=False)
plt.axvline(mae_base, color='red', linestyle='--', label='Traditional Baseline')
plt.title('Performance Evaluation: Traditional Benchmarks vs. Proposed AI')
plt.xlabel('Mean Absolute Error (Lower is Better)')
plt.show()

# 6. EXPORT ASSETS
# Saving the RF model and Scaler for potential Backend integration
joblib.dump(best_estimators["Random Forest (Proposed)"], 'eastern_hararghe_final_model.pkl')
joblib.dump(scaler, 'weather_scaler.pkl')
print("\n✅ Benchmarking complete. Model assets exported.")